//Arrow functions properties comes with ES6. Let's dive in.
let multiply = (numberOne, numberTwo) => numberOne * numberTwo;
console.log(multiply(2, 2));

let concatenationOfTwoStrings = (stringOne, stringTwo) =>
  stringOne + " " + stringTwo;
console.log(concatenationOfTwoStrings("Recep", "Erdogan"));

let addNumbers = (number, numberTwo, numberThree) =>
  number + numberTwo + numberThree;
console.log(addNumbers(1, 2, 3));
